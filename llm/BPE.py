"""
BPE (Byte-Pair Encoding) 算法实现

BPE 是一种常见的子词分割算法，最初用于数据压缩，后被引入 NLP 用于词表构建。
核心思想：迭代地合并语料库中出现频率最高的相邻字符对。

例如语料库包含 "hug"、"pun"、"bug"、"bun"：
1. 统计所有字符对频率
2. 找到最高频的字符对并合并
3. 重复直到达到目标词表大小
"""

# ============================================================
# 依赖导入
# ============================================================

import re               # 正则表达式，用于合并词元对
import collections       # Python 内置集合类型，提供 defaultdict


# ============================================================
# 核心函数
# ============================================================

def get_stats(vocab):
    """
    统计词元对的出现频率

    遍历词表中每个词，统计所有相邻字符对（bigram）的出现次数。

    参数:
        vocab: 词表字典，键为分词后的词（如 "h u g </w>"），
               值为该词的出现频率

    返回:
        pairs: 字典，键为字符对元组 (如 ('h','u')），值为出现总频次
    """
    # 创建一个默认值为 0 的字典，用于统计词元对频率
    pairs = collections.defaultdict(int)

    # 遍历词表中的每个词及其频率
    for word, freq in vocab.items():
        # 将词按空格分割成字符列表
        symbols = word.split()

        # 遍历该词的所有相邻字符对
        for i in range(len(symbols) - 1):
            # 累加该字符对的频率（乘以词的频率）
            pairs[symbols[i], symbols[i + 1]] += freq

    return pairs


def merge_vocab(pair, v_in):
    """
    合并词表中的词元对

    将词表中所有出现的指定字符对合并为一个新的单一词元。

    参数:
        pair: 要合并的字符对元组，如 ('u', 'g')
        v_in: 输入词表字典

    返回:
        v_out: 合并后的新词表字典
    """
    # 初始化输出词表
    v_out = {}

    # 将字符对格式化为字符串用于正则匹配，如 ('u', 'g') -> 'u g'
    bigram = re.escape(' '.join(pair))

    # 构建正则表达式，使用负向前后查找确保字符对不被其他字符分隔
    # (?<!\S) 确保前面不是非空白字符
    # (?!\S) 确保后面不是非空白字符
    p = re.compile(r'(?<!\S)' + bigram + r'(?!\S)')

    # 遍历输入词表中的每个词
    for word in v_in:
        # 替换：将 "u g" 合并为 "ug"（去空格）
        w_out = p.sub(''.join(pair), word)
        # 存入输出词表，保持原频率
        v_out[w_out] = v_in[word]

    return v_out


# ============================================================
# BPE 训练演示
# ============================================================

# 初始化语料库（已分词）
# 每个词末尾的 </w> 是特殊结束符，表示词的边界
# 格式：{"字符1 字符2 字符3 </w>": 频率}
vocab = {
    'h u g </w>': 1,   # "hug" 出现 1 次
    'p u g </w>': 1,   # "pug" 出现 1 次
    'p u n </w>': 1,   # "pun" 出现 1 次
    'b u n </w>': 1     # "bun" 出现 1 次
}

# 设置合并次数
num_merges = 10

# 迭代合并
for i in range(num_merges):
    # 步骤 1：统计当前词表中所有字符对的频率
    pairs = get_stats(vocab)

    # 如果没有词元对了（可能所有字符都已合并），退出循环
    if not pairs:
        break

    # 步骤 2：找到频率最高的字符对
    best = max(pairs, key=pairs.get)

    # 步骤 3：合并最高频的字符对
    vocab = merge_vocab(best, vocab)

    # 打印合并信息
    print(f"第{i + 1}次合并: {best} -> {''.join(best)}")
    print(f"新词表（部分）: {list(vocab.keys())}")
    print("-" * 20)


# ============================================================
# 运行结果预期
# ============================================================
#
# 第1次合并: ('u',) -> ug
# 新词表: ['h ug </w>', 'p ug </w>', 'p u n </w>', 'b u n </w>']
# --------------------------------------------------
# 第2次合并: ('u', 'n') -> un
# 新词表: ['h ug </w>', 'p ug </w>', 'p un </w>', 'b un </w>']
# --------------------------------------------------
# 第3次合并: ('p', 'ug') -> pug
# 新词表: ['h ug </w>', 'pug </w>', 'p un </w>', 'b un </w>']
# --------------------------------------------------
# 第4次合并: ('p', 'un') -> pun
# 新词表: ['h ug </w>', 'pug </w>', 'pun </w>', 'b un </w>']
# --------------------------------------------------
#
# 最终学习的子词: ug, un, pug, pun
# 这样可以泛化到未见过的词，如 "hug" 和 "pun" 的组合
